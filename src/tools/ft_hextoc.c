#include "rgblib.h"

char	ft_hextoc(char c)
{
	if (c >= '0' && c <= '9')
		return (c - '0');
	if (c >= 'a' && c <= 'f')
		return (c - 'a');
	if (c >= 'A' && c <= 'F')
		return (c - 'A');
	return (-1);
}
